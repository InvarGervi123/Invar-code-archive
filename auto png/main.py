import requests
from PIL import Image
from io import BytesIO

# Example card data list (replace these examples with your actual data)
cards = [
    {
        'front_url': 'https://static.wikia.nocookie.net/fallout/images/e/e9/FNV_Gomorrah_2_of_clubs.png',  # התמונה הנכונה
        'back_url': 'https://static.wikia.nocookie.net/fallout/images/9/9d/FNV_Gomorrah_Card_Back.png',
        'output_path': 'gomorrah_2_of_clubs.png'
    },
    # ... קלפים נוספים
]




# Function to download an image from a URL
def download_image(url):
    headers = {'User-Agent': 'Mozilla/5.0'}
    response = requests.get(url, headers=headers)
    response.raise_for_status()
    img = Image.open(BytesIO(response.content)).convert('RGBA')
    img.show()  # להציג את התמונה
    return img


# Function to create a Caravan card image
def create_caravan_card(front_url, back_url, output_path):
    front_img = download_image(front_url).resize((350, 500))
    back_img = download_image(back_url).resize((350, 500))

    # Create the final image (transparent background)
    final_image = Image.new('RGBA', (750, 650), (0, 0, 0, 0))

    # Paste back image on the left and front image on the right
    final_image.paste(back_img, (25, 75), back_img)
    final_image.paste(front_img, (400, 75), front_img)

    final_image.save(output_path)

# Automatically process all cards
for card in cards:
    create_caravan_card(card['front_url'], card['back_url'], card['output_path'])